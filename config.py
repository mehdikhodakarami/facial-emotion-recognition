INPUT_SHAPE = (IMG_SIZE, IMG_SIZE, CHANNELS)

# ─── معماری مدل (قابل تنظیم — سبک/سنگین) ───────────────────────────────────
# نسخهٔ سبک: ۳ مرحله (بدون مرحلهٔ سنگین ۵۱۲) → ~۲.۵M پارامتر، مناسب CPU.
# برای نسخهٔ قوی‌تر روی GPU: STAGE_FILTERS = (64, 128, 256, 512)
STEM_FILTERS     = 64
STAGE_FILTERS    = (64, 128, 256)
BLOCKS_PER_STAGE = 2
HEAD_UNITS       = 256

# ─── هایپرپارامترهای آموزش ─────────────────────────────────────────────────
SEED            = 42      # برای بازتولیدپذیری کامل نتایج
BATCH_SIZE      = 64
EPOCHS          = 45      # epoch بیشتر = همگرایی بهتر cosine + پوشش بیشتر داده
BASE_LR         = 1e-3    # نرخ یادگیری اولیه (AdamW + Cosine schedule)
WARMUP_EPOCHS   = 3       # گرم‌کردن تدریجی LR در ابتدای آموزش
WEIGHT_DECAY    = 1e-4    # تنظیم‌سازی L2 از طریق AdamW
LABEL_SMOOTHING = 0.1     # جلوگیری از بیش‌اطمینانی مدل
DROPOUT_HEAD    = 0.4     # Dropout در بخش طبقه‌بند
EARLY_STOP_PATIENCE = 12  # صبر بیشتر تا epochهای اضافه فرصت بهبود داشته باشند

# هر epoch فقط این تعداد گام (زیرمجموعهٔ تصادفی) را می‌بیند تا روی CPU سریع باشد.
# با reshuffle، در طول epochها کل داده پوشش داده می‌شود. None = کل داده در هر epoch.
#   • سریع‌تر؟  این عدد را کم کن (مثلاً 300) یا IMG_SIZE را کم کن.
#   • دقیق‌تر؟  این عدد را زیاد کن (یا None) و روی GPU IMG_SIZE=96 + مرحلهٔ 512 بگذار.
# روی این لپ‌تاپ (M2 CPU، ~1.5s/step) مقدار 400 ≈ ۱۰ دقیقه در هر epoch.
# با 45 epoch → کل ~۷.۵ ساعت (با EarlyStopping عملاً ~۵ ساعت). خیلی زیر ۲۴ ساعت.
STEPS_PER_EPOCH_CAP = 400
# 400×64 ≈ 25.6k تصویر در هر epoch

# اندازهٔ بافر shuffle (محدود نگه داشته شده برای RAM ۸ گیگ)
SHUFFLE_BUFFER = 2048

# ─── Inference / Threshold ────────────────────────────────────────────────
# آستانهٔ سراسری پیش‌فرض (فقط زمانی استفاده می‌شود که فایل کالیبره‌شده موجود نباشد).
# با 8 کلاس + Label Smoothing، حداکثر softmax سقف ~0.91 دارد؛ پس آستانهٔ پایین‌تر
# منطقی‌تر است و آستانهٔ نهایی به‌صورت هر-کلاس روی validation کالیبره می‌شود.
CONFIDENCE_THRESHOLD = 0.40

# فایل آستانه‌های کالیبره‌شدهٔ هر کلاس (خروجی calibrate_thresholds.py)
THRESHOLDS_PATH = os.path.join(MODELS_DIR, 'thresholds.json')


# ═══════════════════════════════════════════════════════════════════════════
#  تنظیمات استنتاج زنده (وب‌کم) — بدون نیاز به آموزش مجدد
# ═══════════════════════════════════════════════════════════════════════════
#  این پارامترها رفتار وب‌کم را در دنیای واقعی اصلاح می‌کنند. مشکلِ رایج:
#  مدل روی چهره‌های «تنگ و اغراق‌شده» آموزش دیده، اما وب‌کم چهره‌ای «معمولی و
#  با حاشیه» می‌بیند → خروجی به neutral سوگیری می‌کند و زیاد Uncertain می‌زند.

# (۱) هموارسازی زمانی: میانگین احتمالات روی N فریم اخیر.
#     پرش/فلیکر را از بین می‌برد و خروجی را پایدار می‌کند. (0 یا 1 = خاموش)
TEMPORAL_SMOOTHING = 7

# (۲) تصحیح سوگیری کلاس‌ها: احتمال هر کلاس در این ضریب ضرب می‌شود.
#     neutral را کم‌وزن می‌کنیم تا سوگیریِ «همه‌چیز خنثی» جبران شود.
#     <1 یعنی آن کلاس کمتر انتخاب شود، >1 یعنی بیشتر. (همه 1.0 = خاموش)
#     اگر هنوز «همه‌چیز خنثی» بود → neutral را کمتر کن (مثلاً 0.4).
#     اگر بیش‌ازحد احساسات اشتباه نشان داد → neutral را بالاتر ببر (مثلاً 0.65).
CLASS_BIAS = {
    'angry':    1.10,
    'contempt': 1.10,
    'disgust':  1.30,
    'fear':     1.10,
    'happy':    1.00,
    'neutral':  0.50,   # ← مهم‌ترین: کاهش سوگیری به خنثی
    'sad':      1.10,
    'suprise':  1.05,
}


'''
این یک ترفند ریاضی برای «تند کردن» یا «کند کردن» تصمیم‌گیری مدل است.

چه کار می‌کند؟

دمای < ۱: باعث می‌شود اختلاف احتمالات زیاد شود (قاطعیت مدل بالا می‌رود).

دمای > ۱: اختلاف احتمالات کم می‌شود (مدل خجالتی‌تر می‌شود).

مثال: اگر مدل با ۴۰٪ احتمال می‌گوید «شادی»، با دمای ۰.۸، آن احتمال ممکن است به ۶۰٪ تبدیل شود تا مدل راحت‌تر تصمیم بگیرد.
'''
INFERENCE_TEMPERATURE = 1.2

# (۴) ضریب آستانه برای استفادهٔ زنده: آستانه‌های کالیبره‌شده در این عدد ضرب
#     می‌شوند. <1 یعنی سخت‌گیری کمتر → حالات بیشتری نمایش، Uncertain کمتر.
#     اگر هنوز زیاد Uncertain می‌زند → کمترش کن (مثلاً 0.6).
THRESHOLD_SCALE = 0.70

# (۵) حاشیهٔ کادر چهره: تصاویر آموزش چهره را کاملاً پر می‌کنند، پس کادر تنگ‌تر
#     (حاشیهٔ کمتر) با توزیع آموزش هماهنگ‌تر است. قبلاً 0.05 بود. منفی = تنگ‌تر.
FACE_CROP_PADDING = 0.0


def get_class_bias_vector():
    """بردار ضریب سوگیری به‌ترتیب CLASS_NAMES."""
    return [float(CLASS_BIAS.get(c, 1.0)) for c in CLASS_NAMES]


def load_class_thresholds():
    """
    آستانهٔ هر کلاس را برمی‌گرداند: لیستی به طول NUM_CLASSES.
    اگر فایل کالیبره‌شده موجود باشد از آن استفاده می‌شود، وگرنه آستانهٔ سراسری.
    """
    import json
    thresholds = [CONFIDENCE_THRESHOLD] * NUM_CLASSES
    if os.path.exists(THRESHOLDS_PATH):
        try:
            with open(THRESHOLDS_PATH, 'r', encoding='utf-8') as f:
                data = json.load(f)
            per_class = data.get('per_class', {})
            for i, c in enumerate(CLASS_NAMES):
                if c in per_class:
                    thresholds[i] = float(per_class[c])
        except Exception as e:
            print(f"  [هشدار] خواندن thresholds.json ناموفق بود: {e}")
    return thresholds

