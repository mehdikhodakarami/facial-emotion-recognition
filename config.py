# ─── کلاس‌ها ───────────────────────────────────────────────────────────────
# باید دقیقاً با نام پوشه‌های روی دیسک یکی و به‌ترتیب الفبایی باشد
# (همان ترتیبی که image_dataset_from_directory تولید می‌کند).
CLASS_NAMES = [
    'angry', 'contempt', 'disgust', 'fear',
    'happy', 'neutral', 'sad', 'suprise',
]
# نام‌های تمیز برای نمایش در نمودارها و وب‌کم (غلط املایی اصلاح می‌شود)
DISPLAY_NAMES = [
    'Angry', 'Contempt', 'Disgust', 'Fear',
    'Happy', 'Neutral', 'Sad', 'Surprise',
]
NUM_CLASSES = len(CLASS_NAMES)

CLASS_TO_IDX = {c: i for i, c in enumerate(CLASS_NAMES)}
IDX_TO_CLASS = {i: c for i, c in enumerate(CLASS_NAMES)}
IDX_TO_DISPLAY = {i: d for i, d in enumerate(DISPLAY_NAMES)}

# سازگاری با کدهای قدیمی که از EMOTIONS استفاده می‌کردند
EMOTIONS = CLASS_NAMES

# ─── ابعاد تصویر ──────────بق─────────────────────────────────────────────────
IMG_SIZE    = 64
